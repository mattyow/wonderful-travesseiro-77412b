// sync-airtable.js — pulls books from Airtable and writes books.js
// Run with: node sync-airtable.js

const fs = require('fs');
const path = require('path');

// Load .env file
function loadEnv() {
  const envPath = path.join(__dirname, '.env');
  if (!fs.existsSync(envPath)) {
    console.error('No .env file found. Create one with AIRTABLE_TOKEN, AIRTABLE_BASE_ID, and AIRTABLE_TABLE_NAME.');
    process.exit(1);
  }
  const content = fs.readFileSync(envPath, 'utf8');
  content.split('\n').forEach(line => {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith('#')) return;
    const eq = trimmed.indexOf('=');
    if (eq === -1) return;
    const key = trimmed.slice(0, eq).trim();
    const value = trimmed.slice(eq + 1).trim();
    process.env[key] = value;
  });
}

loadEnv();

const TOKEN = process.env.AIRTABLE_TOKEN;
const BASE_ID = process.env.AIRTABLE_BASE_ID;
const TABLE_NAME = process.env.AIRTABLE_TABLE_NAME;

if (!TOKEN || !BASE_ID || !TABLE_NAME) {
  console.error('Missing required .env values: AIRTABLE_TOKEN, AIRTABLE_BASE_ID, AIRTABLE_TABLE_NAME');
  process.exit(1);
}

function cleanIsbn(value) {
  if (!value) return '';
  return String(value)
    .trim()
    .replace(/^=/, '')
    .replace(/^["'\u201C\u2018]+|["'\u201D\u2019]+$/g, '')
    .replace(/[^0-9Xx]/g, '');
}

async function fetchAllRecords() {
  const records = [];
  let offset = null;

  do {
    const url = new URL(`https://api.airtable.com/v0/${BASE_ID}/${encodeURIComponent(TABLE_NAME)}`);
    if (offset) url.searchParams.set('offset', offset);

    const res = await fetch(url.toString(), {
      headers: { Authorization: `Bearer ${TOKEN}` }
    });

    if (!res.ok) {
      const text = await res.text();
      throw new Error(`Airtable API error ${res.status}: ${text}`);
    }

    const data = await res.json();
    records.push(...data.records);
    offset = data.offset;

    console.log(`Fetched ${records.length} records...`);
  } while (offset);

  return records;
}

async function main() {
  console.log('Fetching books from Airtable...');
  const records = await fetchAllRecords();
  console.log(`Total: ${records.length} books`);

  // Try to preserve enriched metadata (genres, themes) from existing books.js
  // by matching on ISBN
  let existingMeta = {};
  const booksPath = path.join(__dirname, 'books.js');
  if (fs.existsSync(booksPath)) {
    try {
      const content = fs.readFileSync(booksPath, 'utf8');
      const match = content.match(/const BOOKS = (\[[\s\S]*\]);?\s*$/);
      if (match) {
        const existingBooks = eval(match[1]);
        existingBooks.forEach(b => {
          if (b.isbn) {
            existingMeta[b.isbn] = {
              genres: b.genres,
              themes: b.themes
            };
          }
        });
        console.log(`Preserving genres/themes for ${Object.keys(existingMeta).length} existing books`);
      }
    } catch (e) {
      console.log('Could not parse existing books.js — proceeding without metadata preservation');
    }
  }

  // Transform Airtable records into book entries
  const books = records.map(r => {
    const f = r.fields;
    const isbn = cleanIsbn(f.ISBN13) || cleanIsbn(f.ISBN);
    const book = {
      title: (f.Title || 'Untitled').trim(),
      author: (f.Author || '').trim()
    };

    const addl = (f['Additional Authors (translators, co-authors, editor)'] || '').trim();
    if (addl) book.additionalAuthors = addl;

    if (isbn) book.isbn = isbn;

    // Preserve enriched metadata if we have it for this ISBN
    const meta = existingMeta[isbn];
    if (meta) {
      if (meta.genres) book.genres = meta.genres;
      if (meta.themes) book.themes = meta.themes;
    } else if (f.Category) {
      // Fall back to Airtable Category field for new books
      const cats = String(f.Category).split(',').map(c => c.trim()).filter(Boolean);
      if (cats.length) book.genres = cats;
    }

    return book;
  });

  // Sort books by title for stable output
  books.sort((a, b) => a.title.localeCompare(b.title));

  // Write books.js
  const output = '// Auto-generated by sync-airtable.js — do not edit manually.\n' +
    '// Edit books in Airtable, then run: node sync-airtable.js\n\n' +
    'const BOOKS = ' + JSON.stringify(books, null, 2) + ';\n';

  fs.writeFileSync(booksPath, output);
  console.log(`\nWrote ${books.length} books to books.js`);
}

main().catch(err => {
  console.error('Error:', err.message);
  process.exit(1);
});